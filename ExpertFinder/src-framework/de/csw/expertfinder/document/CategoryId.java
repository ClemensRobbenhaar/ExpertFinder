package de.csw.expertfinder.document;

import java.io.Serializable;

/**
 * This class implements a composite id for the document - category
 * relation.
 * @author ralph
 */
public class CategoryId implements Serializable {
	private Document document;
	private Category category;
	
	

	/**
	 * @return the document
	 */
	public Document getDocument() {
		return document;
	}

	/**
	 * @param document the document to set
	 */
	public void setDocument(Document document) {
		this.document = document;
	}

	/**
	 * @return the category
	 */
	public Category getCategory() {
		return category;
	}

	/**
	 * @param category the category to set
	 */
	public void setCategory(Category category) {
		this.category = category;
	}

	/**
	 * @see de.csw.expertfinder.document.PersistableEntity#hashCode()
	 */
	@Override
	public int hashCode() {
		return document.hashCode() + 31 * category.hashCode();
	}
	
	/**
	 * @see de.csw.expertfinder.document.PersistableEntity#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		return obj instanceof CategoryId &&
		document.equals(((CategoryId) obj).getDocument()) &&
		category.equals(((CategoryId) obj).getCategory());
	}
}
