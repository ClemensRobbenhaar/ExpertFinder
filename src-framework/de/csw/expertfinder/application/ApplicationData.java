package de.csw.expertfinder.application;

/**
 * Instances of this class store generic persistable application data in
 * key-value fashion. These are not configuration parameters but data that are
 * generated dynamically by the application but should nevertheless persist
 * throughout the application's lifecycle.
 * 
 * @author ralph
 */
public class ApplicationData {
	
	/** Key for the ontology file md5 string */
	public static final String ONTOLOGY_MD5 = "ONTOLOGY_MD5";

	private String key;
	private String value;


	/**
	 * Internal use only.
	 */
	ApplicationData() {
	}

	/**
	 * Constructs an application data key-value pair.
	 * 
	 * @param key
	 * @param value
	 */
	public ApplicationData(String key, String value) {
		super();
		this.key = key;
		this.value = value;
	}

	/**
	 * @return the key
	 */
	public String getKey() {
		return key;
	}

	/**
	 * @param key
	 *            the key to set
	 */
	public void setKey(String key) {
		this.key = key;
	}

	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}

	/**
	 * @param value
	 *            the value to set
	 */
	public void setValue(String value) {
		this.value = value;
	}

}
